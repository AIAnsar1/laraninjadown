                InlineKeyboardButton::make('üá∫üáø UZ', callback_data: 'lang:uz'),
                InlineKeyboardButton::make('üá∑üá∫ RU', callback_data: 'lang:ru'),
                InlineKeyboardButton::make('üá¨üáß ENG', callback_data: 'lang:eng'),
            );

        $bot->sendMessage(
            text: "–í—ã–±–µ—Ä–∏—Ç–µ —è–∑—ã–∫ / Choose your language / Iltimos Bot Tilini Tanlagn",   // –¥–µ—Ñ–æ–ª—Ç–Ω–æ–µ –ø—Ä–∏–≤–µ—Ç—Å—Ç–≤–∏–µ
